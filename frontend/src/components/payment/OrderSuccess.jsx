import { useEffect } from "react";
import { useNavigate, useLocation } from "react-router-dom";
import { CheckCircle } from "lucide-react";
import { motion } from "framer-motion";
import { useCart } from "../LandingPage/CartContext";

export default function OrderSuccess() {
  const navigate = useNavigate();
  const location = useLocation();
  const { clearCart } = useCart();
  const orderNumber = location.state?.orderNumber;

  useEffect(() => {
    // clearCart should be idempotent; also sometimes we clear it when navigating here
    clearCart?.(); // ðŸ§¹ clear cart after order
    window.scrollTo({ top: 0, behavior: "smooth" });
  }, []);

  return (
    <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-green-50 to-white px-6">
      <motion.div
        initial={{ scale: 0.6, opacity: 0 }}
        animate={{ scale: 1, opacity: 1 }}
        transition={{ duration: 0.6, ease: "easeOut" }}
        className="bg-white p-10 rounded-3xl shadow-xl max-w-md w-full text-center"
      >
        {/* âœ… Animated Icon */}
        <motion.div
          initial={{ scale: 0 }}
          animate={{ scale: 1 }}
          transition={{ delay: 0.3, type: "spring", stiffness: 200 }}
          className="flex justify-center mb-6"
        >
          <CheckCircle size={80} className="text-green-600" />
        </motion.div>

        {/* ðŸŽ‰ Text */}
        <motion.h1
          initial={{ y: 20, opacity: 0 }}
          animate={{ y: 0, opacity: 1 }}
          transition={{ delay: 0.5 }}
          className="text-3xl font-bold mb-3"
        >
          Order Placed Successfully!
        </motion.h1>

        <motion.p
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ delay: 0.7 }}
          className="text-gray-500 mb-8"
        >
          Thank you for shopping with <b>REVE CULT</b> ðŸ’–  
          <br />
          Your order will be delivered soon.
          {orderNumber && (
            <div className="mt-3 font-medium text-gray-700">Order Number: <span className="text-pink-600">{orderNumber}</span></div>
          )}
        </motion.p>

        {/* ðŸŽ¯ Buttons */}
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ delay: 0.9 }}
          className="flex flex-col gap-3"
        >
          <button
            onClick={() => navigate("/shop")}
            className="w-full py-3 bg-black text-white rounded-xl font-semibold hover:scale-105 transition"
          >
            Continue Shopping
          </button>

          <button
            onClick={() => navigate("/")}
            className="w-full py-3 border rounded-xl font-semibold hover:bg-gray-100 transition"
          >
            Go to Home
          </button>
        </motion.div>
      </motion.div>
    </div>
  );
}
